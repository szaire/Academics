// Operadores ++ e --
function resetarNum() {
     idade = 20;
}

let idade = 20
console.log(idade++);
console.log(idade);
console.log("");
resetarNum();
console.log(++idade);
console.log(idade);
console.log("");
resetarNum();
console.log(idade--);
console.log(idade);
console.log("");
resetarNum();
console.log(--idade);
console.log(idade);