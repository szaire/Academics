// Operadores de igualdade

// igualdade restrista
console.log(1 === 1); // compara um número com um número
console.log('1' === 1); // compara uma string com um número
console.log('');

// igualdade solta
console.log(1 == 1); // compara um número com um número
console.log('1' == 1); // compara uma string com um número e
// converte para o mesmo valor